// File: wifi_comm.h
#ifndef WIFI_COMM_H
#define WIFI_COMM_H

#include <stdbool.h>
#include <stddef.h>
#include "esp_err.h"

/**
 * @brief Initialise Wi‑Fi in STA mode, scan for APs, and connect to the given
 *        SSID/password.
 *
 * @param ssid      Null‑terminated Wi‑Fi SSID
 * @param password  Null‑terminated Wi‑Fi password
 */
esp_err_t wifi_comm_start(const char *ssid, const char *password);

/**
 * @brief Returns true once the station has obtained an IP address.
 */
bool wifi_is_connected(void);

/**
 * @brief Stops Wi-Fi and deinitializes the Wi-Fi driver.
 */
void wifi_comm_stop(void);



#endif /* WIFI_COMM_H */



































































































































































































































